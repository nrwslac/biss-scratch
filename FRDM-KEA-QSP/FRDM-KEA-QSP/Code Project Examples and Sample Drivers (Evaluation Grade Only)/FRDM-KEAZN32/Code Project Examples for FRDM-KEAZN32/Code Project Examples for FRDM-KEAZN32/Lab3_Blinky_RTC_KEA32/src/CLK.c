/***********************************************************************************************
*
* @brief    CLK_Init - Initialize Core Clock to 32 MHz, Bus Clock to 16 MHz
* @param    none
* @return   none
*
************************************************************************************************/  
void Clk_Init()
{
 ICS_C1|=ICS_C1_IRCLKEN_MASK;       /* Enable the internal reference clock*/
 ICS_C3= 0x90;                      /* Reference clock frequency = 31.25 kHz*/
 while(!(ICS_S & ICS_S_LOCK_MASK)); /* Wait for PLL, running at 32 MHz (1024 * 31.25 kHz) */
 ICS_C2|=ICS_C2_BDIV(1);	        /* BDIV=2, Bus clock = 16 MHz*/
}
